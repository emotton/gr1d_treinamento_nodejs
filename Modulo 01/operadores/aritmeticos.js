/* Aritméticos (+, -, *, /, %, ++, --) */

let x = 100;
console.log("x => ", x);
let y = 22;
console.log("y => ", y);

/* Adição "+" */
console.log("x+y =>",x+y);

/* Substração "-" */
console.log("x-y =>",x-y);

/* Multiplicação "*" */
console.log("x*y =>", x*y);

/* Divisão "/" */
console.log("x/y =>", x/y);

/* Módulo "%" (Resto de uma divisão) */
console.log("x%y =>", x%y);

/* Incremento "++" */
console.log("x++ =>", x++);
console.log("x++ =>", x++);

/* Decremento "--" */
console.log("y-- =>", y--);
console.log("y-- =>", y--);

console.log("++x =>", ++x);

/* Cumulativo */
x+= 2;
console.log("x+= 2 =>", x);

x*= 2;
console.log("x*= 2 =>", x);