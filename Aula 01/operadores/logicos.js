let n1 = 10;
let n2 = 20;
let n3 = 30;

/* Operador AND = && */
console.log("n1 < n2 =>", n1 < n2);
console.log("n1 < n2 && n2 < n3 =>", n1 < n2 && n2 < n3);

/* Operador OR = || */
console.log("n1 == n2 || n3 > n2 =>", n1 == n2 || n3 > n2);

/* Operador de Negação NOT = ! */
console.log("!(n1 > n2)", !(n1 > n2));

let valor = true;
console.log("!valor =>", !valor);